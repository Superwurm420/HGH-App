import { readdirSync, readFileSync, statSync, writeFileSync } from 'node:fs';

const ROOT = new URL('../', import.meta.url);

function writeJson(relativePath, data) {
  writeFileSync(new URL(relativePath, ROOT), `${JSON.stringify(data, null, 2)}\n`, 'utf8');
}

function syncTvSlidesIndex() {
  const files = readdirSync(new URL('./assets/tv-slides/', ROOT), { withFileTypes: true })
    .filter((entry) => entry.isFile())
    .map((entry) => entry.name)
    .filter((name) => /\.(png|jpe?g|webp|gif|svg)$/i.test(name))
    .sort((a, b) => a.localeCompare(b, 'de'));

  writeJson('./assets/tv-slides/slides.json', files);
  return files.length;
}

function syncLatestTimetablePdfReference() {
  const pdfs = readdirSync(new URL('./content/timetables/', ROOT), { withFileTypes: true })
    .filter((entry) => entry.isFile() && /\.pdf$/i.test(entry.name))
    .map((entry) => {
      const stats = statSync(new URL(`./content/timetables/${entry.name}`, ROOT));
      const pattern = entry.name.match(/kw_(\d+)_Hj([12])_(\d{4})_(\d{2})\.pdf$/i);
      const sortKey = pattern
        ? {
            schoolYearStart: Number(pattern[3]),
            schoolYearEnd: Number(pattern[4]),
            halfYear: Number(pattern[2]),
            week: Number(pattern[1]),
          }
        : null;
      return { name: entry.name, mtimeMs: stats.mtimeMs, sortKey };
    })
    .sort((a, b) => {
      if (a.sortKey && b.sortKey) {
        if (b.sortKey.schoolYearStart !== a.sortKey.schoolYearStart) return b.sortKey.schoolYearStart - a.sortKey.schoolYearStart;
        if (b.sortKey.schoolYearEnd !== a.sortKey.schoolYearEnd) return b.sortKey.schoolYearEnd - a.sortKey.schoolYearEnd;
        if (b.sortKey.halfYear !== a.sortKey.halfYear) return b.sortKey.halfYear - a.sortKey.halfYear;
        if (b.sortKey.week !== a.sortKey.week) return b.sortKey.week - a.sortKey.week;
      } else if (a.sortKey || b.sortKey) {
        return a.sortKey ? -1 : 1;
      }
      return b.mtimeMs - a.mtimeMs;
    });

  if (!pdfs.length) return null;
  const latestPdf = pdfs[0].name;

  const timetableJsonPath = new URL('./content/stundenplan.json', ROOT);
  const timetableJson = JSON.parse(readFileSync(timetableJsonPath, 'utf8'));
  timetableJson.meta = { ...(timetableJson.meta || {}), source: latestPdf };
  writeJson('./content/stundenplan.json', timetableJson);

  const pdfRawPath = new URL('./content/stundenplan.pdf.raw.json', ROOT);
  const pdfRaw = JSON.parse(readFileSync(pdfRawPath, 'utf8'));
  pdfRaw.meta = { ...(pdfRaw.meta || {}), source: latestPdf };
  writeJson('./content/stundenplan.pdf.raw.json', pdfRaw);

  return latestPdf;
}

