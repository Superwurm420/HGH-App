import { createHash } from 'node:crypto';
import { readdirSync, writeFileSync } from 'node:fs';

const ROOT = new URL('../', import.meta.url);

function writeJson(relativePath, data) {
  writeFileSync(new URL(relativePath, ROOT), `${JSON.stringify(data, null, 2)}\n`, 'utf8');
}

function syncTvSlidesIndex() {
  const files = readdirSync(new URL('./assets/tv-slides/', ROOT), { withFileTypes: true })
    .filter((entry) => entry.isFile())
    .map((entry) => entry.name)
    .filter((name) => /\.(png|jpe?g|webp|gif|svg)$/i.test(name))
    .sort((a, b) => a.localeCompare(b, 'de'));

  writeJson('./assets/tv-slides/slides.json', files);
  return files.length;
}

const slidesCount = syncTvSlidesIndex();

const ASSETS = [
  './',
  './index.html',
  './content/txt/events/files.txt',
  './app.css',
  './src/app.js',
  './src/modules/timetable-parser.js',
  './src/config/paths.js',
  './src/config/app-constants.js',
  './src/core/create-initial-state.js',
  './src/services/timetable/timetable-source-service.js',
  './src/services/http/http-client.js',
  './src/data/timetable-source.js',
  './content/stundenplan.generated.json',
  './manifest.json',
  './content/kalender.ics',
  './content/README_admin.txt',
  './assets/icons/favicon-school.svg',
  './assets/icons/apple-touch-icon-school.svg',
  './assets/icons/school-logo-header.svg',
  './assets/icons/school-logo-tv.svg',
  './assets/icons/app-icon-192.svg',
  './assets/icons/app-icon-512.svg',
  './assets/icons/app-icon-512-maskable.svg',
  './assets/icons/class-photo-placeholder.svg'
];

const manifestVersion = createHash('sha256')
  .update(JSON.stringify(ASSETS))
  .digest('hex')
  .slice(0, 12);

const content = `// Auto-generated by scripts/generate-sw-assets.mjs\nself.__HGH_SW_MANIFEST_VERSION = '${manifestVersion}';\nself.__HGH_SW_ASSETS = Object.freeze(${JSON.stringify(ASSETS, null, 2)});\n`;

writeFileSync(new URL('./sw-assets.js', ROOT), content, 'utf8');
console.log(`Generated sw-assets.js with ${ASSETS.length} assets (version ${manifestVersion}).`);
console.log(`Updated assets/tv-slides/slides.json with ${slidesCount} slide(s).`);
